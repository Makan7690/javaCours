    private void jButt19ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButt19ActionPerformed
        Num2 = Double.parseDouble(jtxtResult.getText());
        if (op == "+") {
         Result = Num1 + Num2; 
         jtxtResult.setText(String.valueOf(Result));
        }
        else if (op == "-") {
         Result = Num1 - Num2; 
         jtxtResult.setText(String.valueOf(Result));
        }
       else if (op == "*") {
         Result = Num1 * Num2; 
         jtxtResult.setText(String.valueOf(Result));
        }
       else if (op == "/") {
         Result = Num1 / Num2; 
         jtxtResult.setText(String.valueOf(Result));
        }
        
    }//GEN-LAST:event_jButt19ActionPerformed

